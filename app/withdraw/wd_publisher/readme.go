var Readme = `
# Tokopedia Auto Withdraw
Bot ini digunakan untuk melakukan auto withdraw pada tokopedia.

Sebelum Penggunaan:
1. Masukkan licenci pada file "config.yml" sepeti pada contoh "config.yml.example".
2. Masukkan akun tokopedia yang akan di withdraw kedalam file "akun.txt" dengan format "email|password|otp|pin".

Cara Penggunaan:
1. Buka command promt pada directory file.
2. Jalankan Program dengan tambahan argumen "wd". Contoh: "pdc_tools.exe wd".
3. Hasil report akan muncul dalam file "wd_report.csv".
`
